-- --- create user table first ---

-- Central User Table (Profile Data)
CREATE TABLE IF NOT EXISTS public.users (
    id UUID PRIMARY KEY, -- ID generated by supabase Auth
    email TEXT UNIQUE NOT NULL,
    username TEXT,
    email_confirmed_at TIMESTAMPTZ,
    is_approved BOOLEAN DEFAULT FALSE,
    pending_email TEXT,
    pending_email_status INT DEFAULT 0,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ
);

CREATE OR REPLACE VIEW shared.users WITH (security_invoker = true) AS SELECT id, username FROM public.users;

CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger 
LANGUAGE plpgsql 
SECURITY DEFINER SET search_path = public AS $$
BEGIN
  INSERT INTO public.users (id, email, username, email_confirmed_at, is_approved)
  VALUES (
    new.id, 
    new.email,
    COALESCE(new.raw_user_meta_data->>'username', split_part(new.email, '@', 1)),
    new.email_confirmed_at,
    FALSE
  );
  RETURN new;
END;
$$;

-- Trigger aktivieren
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();


-- Wir erweitern die Funktion, um auch Updates zu handhaben
CREATE OR REPLACE FUNCTION public.handle_user_update()
RETURNS trigger 
LANGUAGE plpgsql 
SECURITY DEFINER SET search_path = public AS $$
BEGIN
  UPDATE public.users
  SET 
    email = new.email,
    pending_email = NULLIF(new.email_change, ''),
    pending_email_status = COALESCE(new.email_change_confirm_status, 0),
    email_confirmed_at = new.email_confirmed_at,
    username = COALESCE(new.raw_user_meta_data->>'username', username),
    updated_at = NOW()
  WHERE id = new.id;
  RETURN new;
END;
$$;

-- Ein zweiter Trigger speziell für Updates in auth.users
DROP TRIGGER IF EXISTS on_auth_user_updated ON auth.users;
CREATE TRIGGER on_auth_user_updated
  AFTER UPDATE ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_user_update();







