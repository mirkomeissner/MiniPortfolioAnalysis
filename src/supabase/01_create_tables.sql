-- ==========================================================
-- 1. RLS ÜBERALL AKTIVIEREN
-- ==========================================================
DO $$ 
DECLARE 
    r RECORD;
BEGIN
    FOR r IN (SELECT table_schema, table_name FROM information_schema.tables WHERE table_schema IN ('public', 'shared') AND table_type = 'BASE TABLE') 
    LOOP
        EXECUTE format('ALTER TABLE %I.%I ENABLE ROW LEVEL SECURITY', r.table_schema, r.table_name);        
    END LOOP;
END $$;

-- ==========================================================
-- 2. SHARED SCHEMA: AUTOMATISCHER LESEZUGRIFF
-- ==========================================================
DO $$ 
DECLARE 
    t text;
BEGIN
    FOR t IN (SELECT table_name FROM information_schema.tables WHERE table_schema = 'shared' AND table_type = 'BASE TABLE') 
    LOOP
        EXECUTE format('DROP POLICY IF EXISTS "Authenticated Read Access" ON shared.%I', t);
        EXECUTE format('CREATE POLICY "Authenticated Read Access" ON shared.%I FOR SELECT TO authenticated USING (true)', t);
    END LOOP;
END $$;

-- ==========================================================
-- 3. PUBLIC SCHEMA: BESITZER-ZUGRIFF (DIE NEUE SCHLEIFE)
-- ==========================================================
-- Hier nutzen wir die performante Version OHNE den EXISTS-Check
DO $$ 
DECLARE 
    t text;
    -- Liste deiner User-Tabellen
    tables text[] := ARRAY['accounts', 'transactions', 'user_import_settings', 'incremental_holdings', 'user_holdings_reorganization'];
BEGIN
    FOREACH t IN ARRAY tables LOOP
        EXECUTE format('DROP POLICY IF EXISTS "Users can only access their own %I" ON public.%I', t, t);
        EXECUTE format('CREATE POLICY "Users can only access their own %I" ON public.%I 
                        FOR ALL TO authenticated 
                        USING (auth.uid() = user_id) 
                        WITH CHECK (auth.uid() = user_id)', t, t);
    END LOOP;
END $$;

-- Sonderfall users-Tabelle (id statt user_id)
DROP POLICY IF EXISTS "Users own profile" ON public.users;
CREATE POLICY "Users own profile" ON public.users 
FOR ALL TO authenticated USING (auth.uid() = id);

