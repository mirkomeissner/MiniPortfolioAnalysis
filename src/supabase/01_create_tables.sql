-- Daily snapshot of quantities per user, account, and asset
-- only increments the date if the holdings change
CREATE TABLE IF NOT EXISTS public.incremental_holdings (
    user_id UUID NOT NULL,
    account_code TEXT NOT NULL,
    holding_date DATE NOT NULL,
    isin VARCHAR(12) NOT NULL,
    quantity NUMERIC(20, 8) NOT NULL,
    PRIMARY KEY (user_id, account_code, holding_date, isin),
    CONSTRAINT fk_incremental_holdings_account FOREIGN KEY (user_id, account_code) REFERENCES public.accounts(user_id, account_code) ON DELETE CASCADE,
    CONSTRAINT fk_incremental_holdings_isin FOREIGN KEY (isin) REFERENCES shared.asset_static_data(isin) ON DELETE CASCADE
);
CREATE INDEX IF NOT EXISTS idx_holdings_lookup ON public.incremental_holdings (user_id, account_code, isin, holding_date DESC);


-- view to create daily holdings by filling the gaps in incremental_holdings
CREATE OR REPLACE VIEW public.daily_holdings WITH (security_invoker = true) AS
WITH date_range AS (
    -- 1. Wir ermitteln den Startpunkt und das Ende der Zeitreihe
    SELECT 
        min(holding_date) as start_date, 
        CURRENT_DATE as end_date 
    FROM public.incremental_holdings
),
all_days AS (
    -- 2. Wir generieren jeden einzelnen Tag
    SELECT generate_series(start_date, end_date, '1 day'::interval)::date AS day
    FROM date_range
),
distinct_keys AS (
    -- 3. Wir brauchen alle Kombinationen aus User, Account und ISIN, die existieren
    SELECT DISTINCT user_id, account_code, isin 
    FROM public.incremental_holdings
)
-- 4. Wir verbinden die Tage mit den Keys und suchen den letzten Stand
SELECT 
    k.user_id,
    k.account_code,
    d.day AS holding_date,
    k.isin,
    h.quantity
FROM all_days d
CROSS JOIN distinct_keys k -- Erzeugt für jeden Tag eine Zeile pro Asset
LEFT JOIN LATERAL (
    -- Dieser Teil sucht für jeden Tag den "aktuellsten" Eintrag in der echten Tabelle
    SELECT ih.quantity
    FROM public.incremental_holdings ih
    WHERE ih.user_id = k.user_id
      AND ih.account_code = k.account_code
      AND ih.isin = k.isin
      AND ih.holding_date <= d.day
    ORDER BY ih.holding_date DESC
    LIMIT 1
) h ON TRUE
WHERE h.quantity IS NOT NULL; -- Verhindert Zeilen für Assets vor ihrem ersten Kauf









-- ==========================================================
-- 1. RLS ÜBERALL AKTIVIEREN
-- ==========================================================
DO $$ 
DECLARE 
    r RECORD;
BEGIN
    FOR r IN (SELECT table_schema, table_name FROM information_schema.tables WHERE table_schema IN ('public', 'shared') AND table_type = 'BASE TABLE') 
    LOOP
        EXECUTE format('ALTER TABLE %I.%I ENABLE ROW LEVEL SECURITY', r.table_schema, r.table_name);        
    END LOOP;
END $$;

-- ==========================================================
-- 2. SHARED SCHEMA: AUTOMATISCHER LESEZUGRIFF
-- ==========================================================
DO $$ 
DECLARE 
    t text;
BEGIN
    FOR t IN (SELECT table_name FROM information_schema.tables WHERE table_schema = 'shared' AND table_type = 'BASE TABLE') 
    LOOP
        EXECUTE format('DROP POLICY IF EXISTS "Authenticated Read Access" ON shared.%I', t);
        EXECUTE format('CREATE POLICY "Authenticated Read Access" ON shared.%I FOR SELECT TO authenticated USING (true)', t);
    END LOOP;
END $$;

-- ==========================================================
-- 3. PUBLIC SCHEMA: BESITZER-ZUGRIFF (DIE NEUE SCHLEIFE)
-- ==========================================================
-- Hier nutzen wir die performante Version OHNE den EXISTS-Check
DO $$ 
DECLARE 
    t text;
    -- Liste deiner User-Tabellen
    tables text[] := ARRAY['accounts', 'transactions', 'user_import_settings', 'incremental_holdings'];
BEGIN
    FOREACH t IN ARRAY tables LOOP
        EXECUTE format('DROP POLICY IF EXISTS "Users can only access their own %I" ON public.%I', t, t);
        EXECUTE format('CREATE POLICY "Users can only access their own %I" ON public.%I 
                        FOR ALL TO authenticated 
                        USING (auth.uid() = user_id) 
                        WITH CHECK (auth.uid() = user_id)', t, t);
    END LOOP;
END $$;

-- Sonderfall users-Tabelle (id statt user_id)
DROP POLICY IF EXISTS "Users own profile" ON public.users;
CREATE POLICY "Users own profile" ON public.users 
FOR ALL TO authenticated USING (auth.uid() = id);

-- ==========================================================
-- 4. GRANTS (DAS FUNDAMENT)
-- ==========================================================
-- Wir entziehen anon alles
REVOKE ALL ON ALL TABLES IN SCHEMA public, shared FROM anon;

-- Authenticated darf USAGE
GRANT USAGE ON SCHEMA public, shared TO authenticated;

-- Authenticated darf in public alles mit seinen (durch RLS geschützten) Daten machen
GRANT SELECT, INSERT, UPDATE, DELETE ON ALL TABLES IN SCHEMA public TO authenticated;
GRANT USAGE, SELECT ON ALL SEQUENCES IN SCHEMA public TO authenticated;

-- Authenticated darf in shared nur lesen und Assets vorschlagen
GRANT SELECT ON ALL TABLES IN SCHEMA shared TO authenticated;
GRANT INSERT ON shared.asset_static_data TO authenticated;
